# Definicja funkcji dodawania
def add(a, b):
    return a + b

# Definicja funkcji odejmowania
def subtract(a, b):
    return a - b

# Definicja funkcji mno≈ºenia
def multiply(a, b):
    return a * b

# Definicja funkcji dzielenia
def divide(a, b):
    if b == 0:
        return "Error: Division by zero"
    else:
        return a / b
